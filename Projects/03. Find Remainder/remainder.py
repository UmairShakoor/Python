# This program finds remainder by dividing a to b using % modulus operator

a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

print(f"Remainder of {a} / {b} is:", a % b)

# f" String Data {variable}" this is an efficient way to use data of variables inside the string without the headache of again and again openinig/closing the string

